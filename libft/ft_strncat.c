#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *rez;

	rez = s1;
	while (*s1++)
		;
	--s1;
	while (n--)
		if ((*s1++ = *s2++) == '\0')
			return (rez);
	*s1 = '\0';
	return (rez);
}
