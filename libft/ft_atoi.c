#include "libft.h"

int		ft_atoi(const char *str)
{
	int sign;
	int n;

	sign = 1;
	n = 0;
	while (*str && *str <= ' ')
		str++;
	if (*str == '-' || *str == '+')
		sign = *str++ == '-' ? -1 : 1;
	while (*str && *str >= '0' && *str <= '9')
		n = n * 10 + (*str++ - '0');
	return (sign * n);
}
